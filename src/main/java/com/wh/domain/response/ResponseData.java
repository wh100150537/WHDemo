package com.wh.domain.response;

public class ResponseData {

    //收件寄件人信息
    private Integer receiverSenderId;
    private String name;
    private String phone;
    private Integer type;
    private Integer userId;
    private String province;
    private String city;
    private String area;
    private String detailAddress;
    private String createTime;

    public Integer getReceiverSenderId() {
        return receiverSenderId;
    }

    public void setReceiverSenderId(Integer receiverSenderId) {
        this.receiverSenderId = receiverSenderId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public String getProvince() {
        return province;
    }

    public void setProvince(String province) {
        this.province = province;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public String getDetailAddress() {
        return detailAddress;
    }

    public void setDetailAddress(String detailAddress) {
        this.detailAddress = detailAddress;
    }

    public String getCreateTime() {
        return createTime;
    }

    public void setCreateTime(String createTime) {
        this.createTime = createTime;
    }
}
